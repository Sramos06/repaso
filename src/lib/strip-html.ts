// Extract searchable plain text from an uploaded HTML reviewer. Drops
// script/style blocks, removes tags, decodes a few common entities, collapses
// whitespace. Best-effort — used only for search, never rendered, so imperfect
// extraction is harmless.
export function stripHtml(html: string): string {
  return html
    .replace(/<(script|style)[^>]*>[\s\S]*?<\/\1>/gi, " ")
    .replace(/<[^>]+>/g, " ")
    .replace(/&nbsp;/gi, " ")
    .replace(/&amp;/gi, "&")
    .replace(/&lt;/gi, "<")
    .replace(/&gt;/gi, ">")
    .replace(/&quot;/gi, '"')
    .replace(/&#39;/gi, "'")
    .replace(/\s+/g, " ")
    .trim();
}
